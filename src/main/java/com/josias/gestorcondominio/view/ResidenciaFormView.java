    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        try {
            // Obtém o proprietário selecionado
            int idProprietario = getProprietarioIdSelecionado(boxSelectProprietario);
            if (idProprietario == -1) {
                JOptionPane.showMessageDialog(this, "Por favor, selecione um proprietário válido.", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Cria o objeto do proprietário
            Proprietario proprietario = new Proprietario();
            proprietario.setId(idProprietario);

            // Preenche os dados da residência
            Residencia residencia = new Residencia();
            residencia.setProprietario(proprietario);
            residencia.setCep(txtCep.getText());
            residencia.setNumero(Integer.parseInt(txtNumero.getText()));
            residencia.setRua(txtRua.getText());

            if (id != 0) {
                residencia.setId(id);
                rc.atualizarResidencia(residencia);
                JOptionPane.showMessageDialog(this, "Residência atualizada com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            } else {
                rc.inserirResidencia(residencia);
                JOptionPane.showMessageDialog(this, "Residência adicionada com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            }

            // Fecha a janela após salvar
            this.dispose();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Número inválido. Por favor, insira um valor válido.", "Erro", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro ao salvar a residência: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

