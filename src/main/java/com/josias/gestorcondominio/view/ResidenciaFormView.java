    private void carregarDados() {
        txtRua.setText(rua);
        txtNumero.setText(String.valueOf(numero));
        txtCep.setText(cep);
    }

    private void carregarProprietarios() {
        List<Pessoa> proprietarios = pc.listarPessoas("Proprietario");
        boxSelectProprietario.removeAllItems();

        for (Pessoa p : proprietarios) {
            boxSelectProprietario.addItem(p.getId() + " - " + p.getNome());
        }
    }

    private int getProprietarioIdSelecionado(JComboBox<String> comboBox) {
        String selecionado = (String) comboBox.getSelectedItem();
        if (selecionado != null) {
            try {
                // Divide a string e tenta converter o ID para int
                return Integer.parseInt(selecionado.split(" - ")[0]);
            } catch (NumberFormatException | ArrayIndexOutOfBoundsException e) {
                // Trata erros de conversão ou formato inesperado
                System.out.println("Erro ao extrair o ID: " + e.getMessage());
            }
        }
        // Retorna -1 caso não seja possível obter um ID válido
        return -1;
    }


